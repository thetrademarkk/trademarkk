const asBars = (xs: number[], vols?: number[]): OHLCV[] =>
  xs.map((c, i) => ({
    time: i * 60000,
    open: c,
    high: c,
    low: c,
    close: c,
    volume: vols ? vols[i]! : 0,
  }));

/** Compare actual vs reference but only from `from` (inclusive) — converged tail. */
function assertTailClose(
  actual: readonly number[],
  expected: readonly (number | null)[],
  from: number,
  eps: number,
  label: string
): void {
  expect(actual.length, `${label}: length`).toBe(expected.length);
  for (let i = from; i < expected.length; i++) {
    const e = expected[i]!;
    expect(Number.isNaN(actual[i]!), `${label}[${i}] expected ${e}, got NaN`).toBe(false);
    expect(
      Math.abs(actual[i]! - e),
      `${label}[${i}] |${actual[i]} - ${e}| > ${eps}`
    ).toBeLessThanOrEqual(eps);
  }
}

