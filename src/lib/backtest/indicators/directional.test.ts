describe("ADX / +DI / -DI — golden vs TA-Lib 0.6.8", () => {
  it("+DI matches TA-Lib PLUS_DI within 1e-6", () => {
    assertCloseArray(adx(BARS, 14).plusDI, PLUS_DI14, EPS, "+DI14");
  });

  it("-DI matches TA-Lib MINUS_DI within 1e-6", () => {
    assertCloseArray(adx(BARS, 14).minusDI, MINUS_DI14, EPS, "-DI14");
  });

  it("ADX matches TA-Lib ADX within 1e-4 (full converged tail)", () => {
    assertCloseArray(adx(BARS, 14).adx, ADX14, EPS_ADX, "ADX14");
  });

  it("NaN warmup: +DI/-DI at index period, ADX at 2*period-1", () => {
    const r = adx(BARS, 14);
    expect(nanPrefixLength(r.plusDI)).toBe(14);
    expect(nanPrefixLength(r.minusDI)).toBe(14);
    expect(nanPrefixLength(r.adx)).toBe(27); // 2*14 - 1
  });

  it("gotcha: series too short for an ADX seed -> ADX all-NaN but DI may exist", () => {
    const short = BARS.slice(0, 20); // enough for DI (>=15), not for ADX (<28)
    const r = adx(short, 14);
    expect(r.adx.every(Number.isNaN)).toBe(true);
    expect(Number.isNaN(r.plusDI[14]!)).toBe(false);
  });

  it("gotcha: a perfectly flat series -> +DI=-DI=0, DX=0, ADX=0", () => {
    const flat: OHLCV[] = Array.from({ length: 40 }, (_, i) => ({
      time: i * 60000,
      open: 50,
      high: 50,
      low: 50,
      close: 50,
      volume: 0,
    }));
    const r = adx(flat, 14);
    expect(r.plusDI[14]).toBe(0);
    expect(r.minusDI[14]).toBe(0);
    expect(r.adx[27]).toBe(0);
  });

  it("deterministic: same input -> identical output", () => {
    expectDeterministic(() => adx(BARS, 14).adx);
    expectDeterministic(() => adx(BARS, 14).plusDI);
  });

  it("registry def computes ADX/+DI/-DI over OHLCV", () => {
    const def = directionalIndicators.find((d) => d.id === "adx")!;
    const r = def.compute(BARS, { period: 14 }) as Record<string, number[]>;
    assertCloseArray(r.plusDI!, PLUS_DI14, EPS, "def +DI");
    assertCloseArray(r.minusDI!, MINUS_DI14, EPS, "def -DI");
    assertCloseArray(r.adx!, ADX14, EPS_ADX, "def ADX");
  });
});

