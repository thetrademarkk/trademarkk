describe("TRIMA — golden vs TA-Lib 0.6.8", () => {
  it("odd period (9) matches the reference within 1e-6", () => {
    assertCloseArray(trima(CLOSE, 9), TRIMA9, EPS, "TRIMA9");
  });
  it("even period (10) matches the reference within 1e-6", () => {
    assertCloseArray(trima(CLOSE, 10), TRIMA10, EPS, "TRIMA10");
  });
  it("period 30 matches the reference within 1e-6", () => {
    assertCloseArray(trima(CLOSE, 30), TRIMA30, EPS, "TRIMA30");
  });
  it("NaN warmup prefix is exactly period-1", () => {
    expect(nanPrefixLength(trima(CLOSE, 30))).toBe(29);
    expect(nanPrefixLength(trima(CLOSE, 10))).toBe(9);
    expect(nanPrefixLength(trima(CLOSE, 9))).toBe(8);
  });
  it("gotcha: series shorter than period is all-NaN, aligned to length", () => {
    const out = trima([1, 2, 3], 5);
    expect(out.length).toBe(3);
    expect(out.every(Number.isNaN)).toBe(true);
  });
  it("on a flat series equals the constant (weights sum to 1)", () => {
    const flat = new Array(20).fill(42);
    const out = trima(flat, 10);
    for (let i = 9; i < 20; i++) expect(Math.abs(out[i]! - 42)).toBeLessThanOrEqual(1e-9);
  });
  it("streaming TRIMA reproduces the batch series exactly", () => {
    assertCloseArray(runStream(createTRIMA(10), CLOSE), TRIMA10, EPS, "stream TRIMA10");
  });
  it("deterministic: same input -> identical output", () => {
    expectDeterministic(() => trima(CLOSE, 30));
  });
});

