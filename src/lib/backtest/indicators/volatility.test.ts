describe("ATR — golden vs TA-Lib 0.6.8 (Wilder, period 14)", () => {
  it("matches the reference within 1e-4", () => {
    assertCloseArray(atr(BARS, 14), ATR14, EPS, "ATR14");
  });

  it("NaN warmup prefix is exactly `period` (first ATR at index period)", () => {
    expect(nanPrefixLength(atr(BARS, 14))).toBe(14);
  });

  it("trueRange: TR[0] is NaN (no prior close), TR[i] is the Wilder true range", () => {
    const tr = trueRange(BARS);
    expect(Number.isNaN(tr[0]!)).toBe(true);
    // TR[1] = max(high-low, |high-prevClose|, |low-prevClose|)
    const h = BARS[1]!.high,
      l = BARS[1]!.low,
      pc = BARS[0]!.close;
    expect(tr[1]!).toBeCloseTo(Math.max(h - l, Math.abs(h - pc), Math.abs(l - pc)), 10);
  });

  it("gotcha: a flat series (H==L==C constant) -> ATR is 0 after warmup", () => {
    const flat: OHLCV[] = Array.from({ length: 30 }, (_, i) => ({
      time: i,
      open: 50,
      high: 50,
      low: 50,
      close: 50,
      volume: 0,
    }));
    const out = atr(flat, 14);
    expect(out[14]!).toBe(0);
    expect(out[29]!).toBe(0);
  });

  it("short series (< period+1 bars) -> all NaN", () => {
    expect(atr(BARS.slice(0, 10), 14).every((v) => Number.isNaN(v))).toBe(true);
  });

  it("streaming ATR reproduces the batch series exactly", () => {
    const s = createATR(14);
    const streamed = BARS.map((b) => s.push(b));
    assertCloseArray(streamed, ATR14, EPS, "stream ATR14");
  });

  it("deterministic: same input -> identical output", () => {
    expectDeterministicSeries(() => atr(BARS, 14));
  });

  it("registry def computes ATR over OHLCV bars", () => {
    const def = volatilityIndicators.find((d) => d.id === "atr")!;
    assertCloseArray(def.compute(BARS, { period: 14 }) as number[], ATR14, EPS, "def ATR");
  });
});

